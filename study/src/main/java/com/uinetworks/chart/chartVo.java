package com.uinetworks.chart;

public class chartVo {
	private int id;
	private String country_name;
	private int country_val;
	
	private String x;
	private String y;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getCountry_name() {
		return country_name;
	}
	public void setCountry_name(String country_name) {
		this.country_name = country_name;
	}
	public int getCountry_val() {
		return country_val;
	}
	public void setCountry_val(int country_val) {
		this.country_val = country_val;
	}
	public String getX() {
		return x;
	}
	public void setX(String x) {
		this.x = x;
	}
	public String getY() {
		return y;
	}
	public void setY(String y) {
		this.y = y;
	}
	
	
}
